import type { Messages } from "./messages.ts";

function m(message: string): { message: string } {
  return { message };
}

function ms(
  message: string,
  placeholder: string,
): { message: string; placeholders: Record<string, { content: string }> } {
  return { message, placeholders: { [placeholder]: { content: "$1" } } };
}

